  async getArticles(category?: string) {
    let result = this.articles
    if (category) {
      result = result.filter(a => a.category === category)
    }
    return {
      code: 200,
      data: result.map(a => ({
        id: a.id,
        category: a.category,
        title: a.title,
        preview: a.preview,
        isMemberOnly: a.isMemberOnly
      }))
    }
  }

  async getArticle(id: string) {
    const article = this.articles.find(a => a.id === id)
    if (!article) {
      return { code: 404, msg: '文章不存在' }
    }
    return { code: 200, data: article }
  }

  async searchArticles(keyword: string) {
    const result = this.articles.filter(
      a => a.title.includes(keyword) || a.content.includes(keyword)
    )
    return {
      code: 200,
      data: result.map(a => ({
        id: a.id,
        category: a.category,
        title: a.title,
        preview: a.preview,
        isMemberOnly: a.isMemberOnly
      }))
    }
  }
